# [1] List Items Are Enclosed between  Square Brackets
# [2] List Are Ordered
# [3] List Are Mutable => Add, Delete, Edit
# [4] List Items Is Not Unique
# [5] List Can Have Different Data Types

lst = ["One", "Two", "One", 1, 100.5, True]
print('lst[0] ='+lst[0])
print('lst[-1] ='+lst[-1])
print('lst[-3] ='+lst[-3])
print('lst[1:4] ='+lst[1:4])
print('lst[:4] ='+lst[:4])
print('lst[4:] ='+lst[4:])